public class RightTriangle {
    public static void main(String args[])
    {
        System.out.println("1.Right Triangle:-");
        for(int i=0;i<=6;i++)
        {
            for(int j=i;j>0;j--)
            {
                System.out.print("* ");
            }
            System.out.println("");
        }
    }
    
}
