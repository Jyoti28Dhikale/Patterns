public class DecreasingRightTriangle {
    public static void main(String args[])
    {
        System.out.println("2. Decreasing Right Triangle:-\n");

    for(int j=6;j>0;j--)
        {
            
            for(int i=0;i<j;i++) 
            {
                
                System.out.print("* ");
            }
            System.out.println(" ");
        }
    }
    
}
